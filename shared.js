// ── 성별 라디오 버튼 → hidden select 동기화 ─────────────
function initGenderRadios() {
  const radios = document.querySelectorAll('input[name="login-gender-radio"]');
  const sel    = document.getElementById('login-gender');
  if(!radios.length || !sel) return;

  // 저장된 성별로 초기 선택
  const saved = getGender();
  if(saved) {
    radios.forEach(r => { r.checked = (r.value===saved); });
    sel.value = saved;
  }

  radios.forEach(r => {
    r.addEventListener('change', () => { if(r.checked) sel.value = r.value; });
  });
}
