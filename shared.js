// ── 인증 ───────────────────────────────────────────────
function loadUser() {
  const saved = localStorage.getItem('qmath_user');
  return saved ? JSON.parse(saved) : null;
}
function saveUser(user) { localStorage.setItem('qmath_user', JSON.stringify(user)); }
function removeUser()   { localStorage.removeItem('qmath_user'); }

function renderNavUser() {
  const user = loadUser();
  const loggedOut = document.getElementById('user-area-loggedout');
  const loggedIn  = document.getElementById('user-area-loggedin');
  if(!loggedOut || !loggedIn) return;
  if(user) {
    loggedOut.classList.add('hidden');
    loggedIn.classList.remove('hidden');
    const nameEl = document.getElementById('user-name-display');
    const avatarEl = document.getElementById('user-avatar');
    if(nameEl) nameEl.textContent = user.name;
    if(avatarEl) avatarEl.textContent = user.name[0];
  } else {
    loggedOut.classList.remove('hidden');
    loggedIn.classList.add('hidden');
  }
}

async function doLogin() {
  const name   = (document.getElementById('login-name')?.value || '').trim() || '학생';
  const role   = document.getElementById('login-role')?.value || 'student';
  const gender = document.getElementById('login-gender')?.value || 'boy';
  saveUser({ name, role, gender });
  hideModal('loginModal');
  renderNavUser();
  applyGenderTheme(gender);
  // Supabase 프로필 저장 (백그라운드)
  if (typeof dbSaveProfile === 'function') {
    const supabaseId = await dbSaveProfile(name, role, gender);
    if (supabaseId) saveUser({ name, role, gender, supabaseId });
  }
}
