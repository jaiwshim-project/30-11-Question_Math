function highlightNav() {
  const page = location.pathname.split('/').pop() || 'index.html';
  document.querySelectorAll('[data-nav]').forEach(el => {
    if(el.dataset.nav === page) {
      el.classList.add('text-primary','border-b-2','border-primary');
    }
  });
}

