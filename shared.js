function applyGenderTheme(gender) {
  const theme = GENDER_THEME[gender] || GENDER_THEME.boy;

  // body data attribute
  document.body.setAttribute('data-gender', gender);
  document.body.classList.remove('theme-boy','theme-girl');
  document.body.classList.add(theme.bodyClass);

  // Inject or update theme style
  let styleEl = document.getElementById('gender-theme-style');
  if(!styleEl) {
    styleEl = document.createElement('style');
    styleEl.id = 'gender-theme-style';
    document.head.appendChild(styleEl);
  }

  if(gender === 'boy') {
    styleEl.textContent = `
      body.theme-boy { --theme-accent:#0EA5E9; --theme-light:#E0F2FE; --theme-border:#BAE6FD; }
      body.theme-boy .theme-accent-bg { background:#E0F2FE !important; }
      body.theme-boy .theme-accent-border { border-color:#BAE6FD !important; }
      body.theme-boy #user-avatar { background:linear-gradient(135deg,#0EA5E9,#0369A1) !important; }
    `;
  } else {
    styleEl.textContent = `
      body.theme-girl { --theme-accent:#EC4899; --theme-light:#FDF2F8; --theme-border:#FBCFE8; }
      body.theme-girl .theme-accent-bg { background:#FDF2F8 !important; }
      body.theme-girl .theme-accent-border { border-color:#FBCFE8 !important; }
      body.theme-girl #user-avatar { background:linear-gradient(135deg,#EC4899,#BE185D) !important; }
    `;
  }

  // Update avatar icon in nav
  const avatarEl = document.getElementById('user-avatar');
  if(avatarEl) {
    const user = loadUser();
    avatarEl.textContent = user ? user.name[0] : theme.avatarIcon;
  }

  // Floating decorations
  spawnFloatingDecos(theme.floatEmojis, gender);
}

function spawnFloatingDecos(emojis, gender) {
  // Remove previous
  document.querySelectorAll('.gender-deco').forEach(e=>e.remove());

  // Pick 6 random positions around the page edges
  const positions = [
    {top:'12%', left:'2%'}, {top:'30%', right:'1%'}, {top:'55%', left:'1.5%'},
    {top:'70%', right:'2%'},{top:'88%', left:'3%'},   {top:'15%', right:'2%'},
  ];

  const styleKey = gender==='girl' ? 'decoGirl' : 'decoBoy';
  const anim     = gender==='girl' ? 'floatGirl 4s ease-in-out infinite' : 'floatBoy 4s ease-in-out infinite';

  // Inject keyframes once
  if(!document.getElementById('deco-keyframes')) {
    const kf = document.createElement('style');
    kf.id = 'deco-keyframes';
    kf.textContent = `
      @keyframes floatBoy  {0%,100%{transform:translateY(0) rotate(-5deg)}50%{transform:translateY(-14px) rotate(5deg)}}
      @keyframes floatGirl {0%,100%{transform:translateY(0) scale(1)}50%{transform:translateY(-12px) scale(1.12)}}
    `;
    document.head.appendChild(kf);
  }

  positions.forEach((pos, i) => {
    const el = document.createElement('div');
    el.className = 'gender-deco';
    el.textContent = emojis[i % emojis.length];
    el.style.cssText = `
      position:fixed; font-size:1.6rem; pointer-events:none; z-index:5; opacity:0.45;
      animation:${anim}; animation-delay:${i*0.6}s;
      ${pos.top    ? 'top:'+pos.top+';'   : ''}
      ${pos.bottom ? 'bottom:'+pos.bottom+';' : ''}
      ${pos.left   ? 'left:'+pos.left+';' : ''}
      ${pos.right  ? 'right:'+pos.right+';' : ''}
    `;
    document.body.appendChild(el);
  });
}

